def obwod_kwadratu(a):
    kwadrat_obliczenia = 4 * a
    return kwadrat_obliczenia

def obwod_okregu(r):
    okrag_obliczenia = 2*3,14*r
    return okrag_obliczenia

def obwod_trapezu(a,b,c):
    trapez_obliczenia = a + b + 2*c
    return trapez_obliczenia

def obwod_prostokata(a,b):
    prostokat_obliczenia = 2*a + 2*b
    return prostokat_obliczenia